#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*next;

	while (*lst != NULL)
	{
		next = (*lst)->next;
		if (del != NULL && (*lst)->content != NULL)
			(del)((*lst)->content);
		free(*lst);
		*lst = next;
	}
}
